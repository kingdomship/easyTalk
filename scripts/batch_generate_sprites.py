# ── Helpers ───────────────────────────────────────────────────────

def _count_pixels(grid: list[str]) -> int:
    return sum(1 for row in grid for c in row if c != "0")


def _validate_sprite(sprite: dict) -> tuple[bool, str]:
    """Validate a single sprite. Returns (ok, reason)."""
    if not isinstance(sprite.get("name"), str) or not sprite["name"]:
        return False, "missing name"

    grid = sprite.get("grid", [])
    if len(grid) != GRID_SIZE:
        return False, f"grid has {len(grid)} rows, want {GRID_SIZE}"
    for i, row in enumerate(grid):
        if not isinstance(row, str) or len(row) != GRID_SIZE:
            return False, f"row {i} length != {GRID_SIZE}"
        if not all(c in "0123456789" for c in row):
            return False, f"row {i} has invalid chars"

    pixels = _count_pixels(grid)
    if pixels < 5:
        return False, f"only {pixels} non-zero pixels (min 5)"

    palette = sprite.get("palette", [])
    if len(palette) < 2 or palette[0] != "transparent":
        return False, "palette[0] must be transparent"
    for c in palette[1:]:
        if not re.match(r"^#[0-9a-fA-F]{6}$", str(c)):
            return False, f"invalid color: {c}"

    keywords = sprite.get("keywords", [])
    if not isinstance(keywords, list) or len(keywords) < 2:
        return False, "need at least 2 keywords"
    if sprite["name"] not in keywords:
        return False, "name not in keywords"

    return True, "ok"


def _load_existing(category: str) -> tuple[list, set]:
    """Load existing sprites from a category JSON file. Returns (list, set_of_names)."""
    path = os.path.join(SPRITE_DIR, f"{category}.json")
    if not os.path.exists(path):
        return [], set()
    try:
        with open(path, encoding="utf-8") as f:
            sprites = json.load(f)
        names = {s.get("name") for s in sprites if isinstance(s, dict)}
        return sprites, names
    except Exception as e:
        logger.error("Failed to load %s: %s", path, e)
        return [], set()


def _save_sprites(category: str, sprites: list):
    """Save sprites list to category JSON file."""
    path = os.path.join(SPRITE_DIR, f"{category}.json")
    os.makedirs(SPRITE_DIR, exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(sprites, f, ensure_ascii=False, indent=2)


def _call_llm(prompt: str, temperature: float = 0.7) -> str | None:
    """Call LLM and return raw content, or None on failure."""
    try:
        resp = get_llm().chat.completions.create(
            model=get_llm_model(),
            messages=[
                {"role": "system", "content": SPRITE_SYSTEM_PROMPT},
                {"role": "user", "content": prompt},
            ],
            temperature=temperature,
            max_tokens=16384,
            timeout=60.0,
        )
        return resp.choices[0].message.content
    except Exception as e:
        logger.error("LLM call failed: %s", e)
        return None


def _parse_sprites(raw: str) -> list[dict]:
    """Parse LLM response into list of sprite dicts."""
    start = raw.find("[")
    end = raw.rfind("]") + 1
    if start < 0 or end <= start:
        return []
    try:
        data = json.loads(raw[start:end])
        if isinstance(data, list):
            return [s for s in data if isinstance(s, dict) and s.get("grid")]
    except json.JSONDecodeError:
        pass
    return []


def _generate_batch(names_and_keywords: list[tuple[str, list[str]]],
                    temperature: float = 0.7) -> list[dict]:
    """Generate a batch of sprites via LLM. Each item is (name, [keywords])."""
    names = [n for n, _ in names_and_keywords]
    prompt = (
        f"生成以下{len(names)}个16×16像素精灵：\n"
        + "\n".join(f"- {n}" for n in names)
        + "\n确保每个精灵keywords的第一项就是它的中文名称。"
    )
    raw = _call_llm(prompt, temperature)
    if not raw:
        return []

    sprites = _parse_sprites(raw)
    if not sprites:
        logger.warning("No valid sprites parsed from LLM response (len=%d)", len(raw) if raw else 0)
        # Log a snippet of the raw response for debugging
        logger.info("Raw response (first 800 chars): %s", (raw or "")[:800])
        return []

    # Debug: log grid stats for each parsed sprite
    for s in sprites:
        g = s.get("grid", [])
        pixels = _count_pixels(g) if g else 0
        logger.info("Parsed '%s': grid_len=%d, pixels=%d", s.get("name"), len(g), pixels)

    # Match sprites back to their intended keywords
    result = []
    for sprite in sprites:
        name = sprite.get("name", "")
        matched = None
        for n, kws in names_and_keywords:
            if name == n or name in kws:
                matched = kws
                break
        if not matched:
            # Try fuzzy: if sprite name contains or is contained by a target name
            for n, kws in names_and_keywords:
                if name and (n in name or name in n):
                    matched = kws
                    break
        if matched:
            sprite["keywords"] = matched
            sprite["size"] = GRID_SIZE
            # Set reasonable defaults if missing
            sprite.setdefault("cell_scale", 2.0)
            sprite.setdefault("duration", 3)
            sprite.setdefault("spread", 0.7)
            sprite.setdefault("weight", 0.5)
            sprite.setdefault("count", 2)
            result.append(sprite)
        else:
            logger.warning("Sprite '%s' doesn't match any requested name", name)

    return result


# ── Main logic ────────────────────────────────────────────────────

def process_category(category: str, force: bool = False, dry_run: bool = False):
    """Generate all sprites for a single category."""
    entries = CATEGORIES.get(category, [])
    if not entries:
        logger.error("Unknown category: %s", category)
        return

    existing, existing_names = _load_existing(category)
    logger.info("[%s] %d existing, %d target", category, len(existing), len(entries))

    # Filter out already-generated sprites
    pending = [(n, kw) for n, kw in entries if force or n not in existing_names]
    if not pending:
        logger.info("[%s] all sprites already exist, skipping", category)
        return

    logger.info("[%s] %d sprites to generate in %d batches",
                category, len(pending), (len(pending) + BATCH_SIZE - 1) // BATCH_SIZE)

    if dry_run:
        for n, kw in pending[:10]:
            logger.info("  would generate: %s → %s", n, kw)
        if len(pending) > 10:
            logger.info("  ... and %d more", len(pending) - 10)
        return

    all_sprites = list(existing)  # start with existing
    total_generated = 0
    total_failed = 0

    for batch_start in range(0, len(pending), BATCH_SIZE):
        batch = pending[batch_start:batch_start + BATCH_SIZE]
        batch_num = batch_start // BATCH_SIZE + 1
        total_batches = (len(pending) + BATCH_SIZE - 1) // BATCH_SIZE

        logger.info("[%s] batch %d/%d: %s", category, batch_num, total_batches,
                    ", ".join(n for n, _ in batch))

        sprites = None
        for attempt in range(MAX_RETRIES):
            temp = 0.7 - attempt * 0.15  # 0.7 → 0.55 → 0.4
            sprites = _generate_batch(batch, temperature=max(temp, 0.2))
            if sprites:
                # Validate
                valid = []
                for s in sprites:
                    ok, reason = _validate_sprite(s)
                    if ok:
                        valid.append(s)
                    else:
                        logger.warning("  skip '%s': %s", s.get("name", "?"), reason)
                if valid:
                    all_sprites.extend(valid)
                    total_generated += len(valid)
                    _save_sprites(category, all_sprites)
                    logger.info("  saved %d sprites (total: %d)", len(valid), len(all_sprites))
                    break
                else:
                    logger.warning("  attempt %d: all %d sprites failed validation", attempt + 1, len(sprites))
            else:
                logger.warning("  attempt %d: no sprites returned", attempt + 1)
        else:
            # All retries exhausted
            total_failed += len(batch)
            for n, _ in batch:
                logger.error("  FAILED: %s (all %d retries)", n, MAX_RETRIES)

        # Small delay to avoid rate limits
        time.sleep(1.0)

    logger.info("[%s] done: %d generated, %d failed, %d total",
                category, total_generated, total_failed, len(all_sprites))


def main():
    parser = argparse.ArgumentParser(description="Batch generate 48x48 pixel sprites via LLM")
    parser.add_argument("--category", type=str, help="Process a single category")
    parser.add_argument("--force", action="store_true", help="Regenerate all sprites (ignore existing)")
    parser.add_argument("--dry-run", action="store_true", help="Show what would be generated without API calls")
    args = parser.parse_args()

    categories_to_process = [args.category] if args.category else list(CATEGORIES.keys())

    logger.info("Categories: %s", ", ".join(categories_to_process))
    logger.info("Total target sprites: %d", sum(len(CATEGORIES[c]) for c in categories_to_process))
    logger.info("Batch size: %d, Max retries: %d", BATCH_SIZE, MAX_RETRIES)

    if args.dry_run:
        logger.info("DRY RUN — no API calls will be made")

    for cat in categories_to_process:
        process_category(cat, force=args.force, dry_run=args.dry_run)

    logger.info("All done.")


if __name__ == "__main__":
    main()
